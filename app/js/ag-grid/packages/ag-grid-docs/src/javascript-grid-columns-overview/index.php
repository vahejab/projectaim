    <h1>Columns Overview</h1>

    <p class="lead">
        A column is a vertical list of cells that represent a field of the displayed data.
    </p>

    <p>
        This section outlines column functionality as follows:
    </p>

    <ul>
        <li>
            <a href="../javascript-grid-column-definitions/">Column Definitions</a>:
            Columns are defined using column definitions. Typically a grid is configured
            by providing a list of column definitions.
        </li>
        <li>
            <a href="../javascript-grid-column-header/">Column Headers</a>:
            Each column has a head with typically the column name.
        </li>
        <li>
            <a href="../javascript-grid-grouping-headers/">Column Groups</a>:
            Columns can be grouped to have multiple levels of column headers.
        </li>
        <li>
            <a href="../javascript-grid-resizing/">Column Sizing</a>:
            Manage the size (width) of columns.
        </li>
        <li>
            <a href="../javascript-grid-column-moving/">Column Moving</a>:
            Columns can be moved, both by the user and also the application via the API.
        </li>
        <li>
            <a href="../javascript-grid-pinning/">Column Pinning</a>:
            Columns can be pinned to stay either to the left or the right
            regardless of what horizontal scrolling the user applies to the grid.
        </li>
        <li>
            <a href="../javascript-grid-column-spanning/">Column Spanning</a>:
            Cells can span across multiple columns.
        </li>
        <li>
            <a href="../javascript-grid-column-animation/">Column Animation</a>:
            Columns can use CSS transitions to animate between locations as columns are moved.
        </li>
    </ul>

