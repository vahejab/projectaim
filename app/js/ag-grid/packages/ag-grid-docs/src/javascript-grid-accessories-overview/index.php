    <h1>Grid Accessories Overview</h1>

    <p class="lead">
        The grid provides many additional functions beyond the simple display of rows and columns.
        Some of these additional functions require UI that are provided in or around the grid.
        These additional UI pieces are known as Grid Accessories.
    </p>

    <p>
        The following is a list of grid accessories:
    </p>

    <ul>
        <li>
            <a href="../javascript-grid-tool-panel/">Tool Panels</a>:
            Tool panels appear on side of the grid and offer addition functionality outside of the core
            grid, eg column and filter management.
        </li>
        <li>
            <a href="../javascript-grid-column-menu/">Column Menu</a>:
            The column menu is accessed from each column header.
        </li>
        <li>
            <a href="../javascript-grid-context-menu/">Context Menu</a>:
            The context menu is accessed by right clicking on the grid.
        </li>
        <li>
            <a href="../javascript-grid-status-bar/">Status Bar</a>:
            The status bar appears below the grid,
        </li>
        <li>
            <a href="../javascript-grid-overlays/">Overlays</a>:
            Overlays appear on top of the grid e.g. "Rows Loading" message while the grid has no rows.
        </li>
    </ul>


